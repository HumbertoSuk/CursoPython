# Ejemplo de operaciones matemáticas, de comparación y lógicas en Python

# Operaciones matemáticas
numero1 = 20
numero2 = 5
suma = numero1 + numero2
resta = numero1 - numero2
multiplicacion = numero1 * numero2
division = numero1 / numero2
modulo = numero1 % numero2
potencia = numero1 ** numero2

# Operaciones de comparación
igual = numero1 == numero2
distinto = numero1 != numero2
mayor_que = numero1 > numero2
menor_que = numero1 < numero2
mayor_o_igual_que = numero1 >= numero2
menor_o_igual_que = numero1 <= numero2

# Operaciones lógicas
condicion1 = True
condicion2 = False
and_logico = condicion1 and condicion2
or_logico = condicion1 or condicion2
not_logico = not condicion1

# Mostrar resultados
print("Operaciones matemáticas:")
print("Suma:", suma)
print("Resta:", resta)
print("Multiplicación:", multiplicacion)
print("División:", division)
print("Módulo:", modulo)
print("Potencia:", potencia)

print("\nOperaciones de comparación:")
print("Igual:", igual)
print("Distinto:", distinto)
print("Mayor que:", mayor_que)
print("Menor que:", menor_que)
print("Mayor o igual que:", mayor_o_igual_que)
print("Menor o igual que:", menor_o_igual_que)

print("\nOperaciones lógicas:")
print("AND lógico:", and_logico)
print("OR lógico:", or_logico)
print("NOT lógico:", not_logico)
